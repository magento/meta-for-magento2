namespace Meta\BusinessExtension\Model\Api;

use Magento\Framework\ObjectManagerInterface;

class CoreConfigFactory
{
    /**
     * Object Manager instance
     *
     * @var ObjectManagerInterface
     */
    public ObjectManagerInterface $objectManager;

    /**
     * @param ObjectManagerInterface $objectManager
     */
    public function __construct(ObjectManagerInterface $objectManager)
    {
        $this->objectManager = $objectManager;
    }

    /**
     * Create class instance with specified parameters
     *
     * @param  array $data
     * @return CoreConfig
     */
    public function create(array $data = []): CoreConfig
    {
        return $this->objectManager->create(CoreConfig::class, $data);
    }
}
